#include <iostream>
using namespace std;
int main() {
    //we are learning arithmatic operators
    int a = 20; int b = 10;
    cout << "a + b = " << a + b << endl;
    cout << "a - b = " << a - b << endl;
    cout << "a * b = " << a * b << endl;
    cout << "a / b = " << a / b << endl;
    cout << "a % b = " << a % b << endl;
    cout << "a = " << a++ << endl;
    cout << "a  = " << a-- << endl;
    cout << " b = " <<  b++ << endl;
    cout << " b = " << b-- << endl;
    cout << "a  = " << ++a << endl;
    cout << "a % b = " << --a << endl;
    cout << "b = " << ++b << endl;
    cout << " b = " << --b << endl;


    return 0;
}